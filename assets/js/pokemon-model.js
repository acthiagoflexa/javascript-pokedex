
class Pokemon {
    constructor(number, name, types, photo, height, weight, abilities, speciesName) {
        this.number = number;
        this.name = name;
        this.types = types;
        this.photo = photo;
        this.height = height;
        this.weight = weight;
        this.abilities = abilities;
        this.speciesName = speciesName;
    }
}